function 构图() {
  新画布(500, 500);
  角度模式(角度());
  方形模式(中心());
  不填充();

  //保存帧("螺旋_", "png", 1, 22);
}

function 绘制() {
  背景色(255);
  偏移(250, 250);

  for (var i = 20; i > 0; i--) {
    保存状态();
    var 边长 = (帧序号() + i * 宽度() / 20) % 宽度();
    旋转(边长 / 2);  // 旋转角度
    笔宽(边长 / 15);
    笔色(0, 按比例放缩(边长, 0, 宽度(), 255, 0));
    长方形(0, 0, 边长, 边长);
    恢复状态();
  }
}